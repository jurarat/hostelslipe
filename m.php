                <h2>The Resort</h2>
                	                    
                    <p></p>
                    <p>
                       Hua Hin is one of the most visited destinations in Thailand. In the past, its charm was peaceful environment and clean beach. Nowadays, the beauty of Hua Hin has been changed to a city living due to numbers of new developments and residential projects. However, The Blue Sky Hua Hin still preserves HuaHin’s charm. The resort situated in Khao Tao area where the beach is still beautiful, the weather is fulfilled by gentle wind and the sea remains clear with the bright blue sky.</p>
                       <p align="center"><img src="img/theresort-img.jpg" width="50%"></p>
                       <p>
The concept of The Blue Sky Hua Hin is to combine Modern architecture with nature by the sea with each room type has its own characteristic and design. The result is an overwhelming experience of resort living.</p>
<p>Although, our newest resort, The Blue Sky Hua Hin, may be different in terms of concept & design from our previous resorts. However, we stay under the same BLUE SKY.</p>
                    
                                        
